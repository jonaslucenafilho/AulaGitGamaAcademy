package br.com.tipos.primitivos;

public class TiposPrimitivos {
	public static void main(String[] args) {
		byte tipoByte = 123;
		short tipoShort = 12345;
		char tipoChar = 'a';
		float tipoFloat = 2.1f;
		double tipoDouble = 1.52;
		int tipoInt = 5564216;
		long tipoLong = 1598410525l;
		boolean tipoBoolean = true;
		
		System.out.println("Valor do tipoByte = " + tipoByte +
				"\nValor do tipoShort = " + tipoShort +
				"\nValor do tipoChar = " + tipoChar +
				"\nValor do tipoFloat = " + tipoFloat +
				"\nValor do tipoDouble = " + tipoDouble +
				"\nValor do tipoInt = " + tipoInt +
				"\nValor do tipoLong = " + tipoLong +
				"\nValor do tipoBoolean = " + tipoBoolean);	
	}
}
